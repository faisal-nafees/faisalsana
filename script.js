// Global variables
let currentLang = 'en';
let bgAudio = null;
let isAudioPlaying = false;
let galleryImages = [];
let currentGalleryIndex = 0;

$(document).ready(function() {
  // Check local storage for language
  if (localStorage.getItem('lang')) {
    currentLang = localStorage.getItem('lang');
  }
  setLanguage(currentLang);

  // Check local storage for theme
  const savedTheme = localStorage.getItem('theme') || 'light';
  $('html').attr('data-theme', savedTheme);
  if (savedTheme === 'dark') {
    $('#dark-mode-toggle').find('i').removeClass('fa-moon').addClass('fa-sun');
  }

  // Preloader Dissolve
  setTimeout(function() {
    $('#preloader').css('opacity', 0);
    setTimeout(function() {
      $('#preloader').css('display', 'none');
    }, 800);
  }, 1800);

  // Initialize Background Audio (Placeholder song)
  bgAudio = new Audio('https://www.soundhelix.com/examples/mp3/SoundHelix-Song-8.mp3');
  bgAudio.loop = true;
  bgAudio.volume = 0.5;

  // Initialize Particle System (Falling Petals and Sparkles)
  startParticles();

  // Set up Gallery image list for Lightbox navigation
  $('.gallery-item').each(function(index) {
    galleryImages.push({
      src: $(this).find('img').attr('src'),
      title: $(this).find('img').attr('alt')
    });
    $(this).attr('data-index', index);
  });

  // Countdown Date setup (e.g. March 18, 2027)
  const targetDate = new Date('March 18, 2027 16:00:00').getTime();
  initCountdown(targetDate);

  // Initialize Event Listeners
  initEventListeners();
  
  // Start typewriter quote
  typeQuote(currentLang);
});

// Event Listeners Registration
function initEventListeners() {
  // Envelope click to open invitation
  $('.wax-seal, #envelope-modal').on('click', function(e) {
    e.stopPropagation();
    $('.envelope-container').addClass('open');
    
    // Play audio since user interacted
    playAudio();
    
    // Fade out modal and show site after animation
    setTimeout(function() {
      $('#envelope-modal').css('opacity', 0);
      setTimeout(function() {
        $('#envelope-modal').css('display', 'none');
        // Initialize AOS here so animations trigger when site is revealed
        if (typeof AOS !== 'undefined') {
          AOS.init({
            duration: 1000,
            once: true,
            offset: 120
          });
        }
      }, 1000);
    }, 1800);
  });

  // Language Toggle Button Click
  $('#lang-toggle, #header-lang-indicator').on('click', function() {
    currentLang = currentLang === 'en' ? 'ur' : 'en';
    setLanguage(currentLang);
    typeQuote(currentLang);
  });

  // Dark Mode Toggle
  $('#dark-mode-toggle').on('click', function() {
    const currentTheme = $('html').attr('data-theme') || 'light';
    const newTheme = currentTheme === 'light' ? 'dark' : 'light';
    $('html').attr('data-theme', newTheme);
    localStorage.setItem('theme', newTheme);
    $(this).find('i').toggleClass('fa-moon fa-sun');
  });

  // Music Controller Click
  $('#music-toggle').on('click', function() {
    if (isAudioPlaying) {
      pauseAudio();
    } else {
      playAudio();
    }
  });

  // Smooth Scrolling for Navbar Links
  $('a[href^="#"]').on('click', function(e) {
    e.preventDefault();
    const target = $(this.getAttribute('href'));
    if (target.length) {
      $('html, body').stop().animate({
        scrollTop: target.offset().top - 80
      }, 1000);
    }
  });

  // Sticky Header on Scroll
  $(window).on('scroll', function() {
    if ($(window).scrollTop() > 50) {
      $('header').addClass('shadow-md bg-opacity-95').removeClass('bg-opacity-80');
    } else {
      $('header').removeClass('shadow-md bg-opacity-95').addClass('bg-opacity-80');
    }
  });

  // Lightbox Open
  $('.gallery-item').on('click', function(e) {
    e.preventDefault();
    currentGalleryIndex = parseInt($(this).attr('data-index'));
    openLightbox(currentGalleryIndex);
  });

  // Lightbox Close
  $('#lightbox-close, #lightbox').on('click', function(e) {
    if (e.target.id === 'lightbox' || e.target.id === 'lightbox-close' || $(e.target).closest('#lightbox-close').length) {
      $('#lightbox').removeClass('active');
      $('body').removeClass('overflow-hidden');
    }
  });

  // Lightbox Next/Prev Navigation
  $('#lightbox-next').on('click', function(e) {
    e.stopPropagation();
    currentGalleryIndex = (currentGalleryIndex + 1) % galleryImages.length;
    updateLightbox(currentGalleryIndex);
  });

  $('#lightbox-prev').on('click', function(e) {
    e.stopPropagation();
    currentGalleryIndex = (currentGalleryIndex - 1 + galleryImages.length) % galleryImages.length;
    updateLightbox(currentGalleryIndex);
  });

  // RSVP Form submission and frontend validation
  $('#rsvp-form').on('submit', function(e) {
    e.preventDefault();
    
    // Clear previous errors
    $('.error-msg').remove();
    $('#rsvp-form input, #rsvp-form select').removeClass('border-red-500');

    const name = $('#rsvp-name').val().trim();
    const guests = $('#rsvp-guests').val();
    const status = $('input[name="rsvp-status"]:checked').val();
    const message = $('#rsvp-message').val().trim();

    let hasErrors = false;

    if (!name) {
      showFieldError($('#rsvp-name'), translations[currentLang].error_empty_name);
      hasErrors = true;
    }
    if (!guests) {
      showFieldError($('#rsvp-guests'), translations[currentLang].error_guests);
      hasErrors = true;
    }
    if (!status) {
      showFieldError($('#status-group'), translations[currentLang].error_status);
      hasErrors = true;
    }

    if (hasErrors) return;

    // Show loading state
    const submitBtn = $(this).find('button[type="submit"]');
    const originalBtnText = submitBtn.html();
    submitBtn.prop('disabled', true).html('<i class="fas fa-spinner fa-spin mr-2"></i>' + translations[currentLang].sending);

    // Simulate server request delay
    setTimeout(function() {
      // Reset button
      submitBtn.prop('disabled', false).html(originalBtnText);
      
      // Open Success Modal
      $('#success-modal-title').text(translations[currentLang].success_title);
      $('#success-modal-desc').text(translations[currentLang].success_desc);
      $('#success-modal').fadeIn(400).css('display', 'flex');

      // Trigger Confetti effect
      if (typeof confetti !== 'undefined') {
        confetti({
          particleCount: 150,
          spread: 80,
          origin: { y: 0.6 }
        });
      }
      
      // Reset form
      $('#rsvp-form')[0].reset();
    }, 1500);
  });

  // Close Success RSVP Modal
  $('#close-success-modal').on('click', function() {
    $('#success-modal').fadeOut(400);
  });
}

// Show validation field error helper
function showFieldError(element, message) {
  element.addClass('border-red-500');
  const errorElement = $('<p class="error-msg text-red-500 text-xs mt-1 font-medium">' + message + '</p>');
  element.after(errorElement);
}

// Sound controllers
function playAudio() {
  if (bgAudio) {
    bgAudio.play().then(() => {
      isAudioPlaying = true;
      $('#music-toggle').find('i').removeClass('fa-volume-mute').addClass('fa-volume-high');
      $('#music-toggle').addClass('animate-spin');
    }).catch(error => {
      console.log("Autoplay prevented:", error);
    });
  }
}

function pauseAudio() {
  if (bgAudio) {
    bgAudio.pause();
    isAudioPlaying = false;
    $('#music-toggle').find('i').removeClass('fa-volume-high').addClass('fa-volume-mute');
    $('#music-toggle').removeClass('animate-spin');
  }
}

// Update translations across the DOM
function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  const textDict = translations[lang];

  $('[data-translate]').each(function() {
    const key = $(this).attr('data-translate');
    if (textDict[key]) {
      if ($(this).is('input, textarea')) {
        $(this).attr('placeholder', textDict[key]);
      } else {
        $(this).html(textDict[key]);
      }
    }
  });

  // Update header text indicator
  $('#header-lang-indicator').text(lang.toUpperCase());

  // Dynamic layout directions
  if (lang === 'ur') {
    $('html').attr('dir', 'rtl').addClass('lang-ur');
  } else {
    $('html').attr('dir', 'ltr').removeClass('lang-ur');
  }

  // Reload AOS to handle offset switches if any
  if (typeof AOS !== 'undefined') {
    AOS.refresh();
  }
}

