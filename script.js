function typeQuote(lang) {
  const text = lang === 'en' 
    ? '"And We created you in pairs." — Surah An-Naba, 78:8' 
    : '”اور ہم نے تمہیں جوڑوں میں پیدا کیا۔“ — سورۃ النبأ، 78:8';
  
  let index = 0;
  $('#typing-quote').html('');
  
  if (window.typingInterval) clearInterval(window.typingInterval);
  
  window.typingInterval = setInterval(() => {
    if (index < text.length) {
      $('#typing-quote').append(text.charAt(index));
      index++;
    } else {
      clearInterval(window.typingInterval);
    }
  }, 50);
}

// Particle System: Falling rose petals and gold stars
function startParticles() {
  setInterval(function() {
    const types = ['rose-petal', 'rose-petal sparkle'];
    const type = types[Math.floor(Math.random() * types.length)];
    const $petal = $('<div class="' + type + '"></div>');
    
    // Random sizes, left position, and falling speed
    const size = Math.random() * 15 + 8;
    const left = Math.random() * $(window).width();
    const duration = Math.random() * 5 + 6; // 6s to 11s fall duration
    
    $petal.css({
      width: size + 'px',
      height: size + 'px',
      left: left + 'px',
      top: '-50px',
      animationDuration: duration + 's'
    });
    
    $('body').append($petal);
    
    // Safely remove element after animation completes
    setTimeout(() => {
      $petal.remove();
    }, duration * 1000);
  }, 450); // Spawning frequency
}

// Circular Countdown Logic
function initCountdown(targetTime) {
  const circumference = 251.2; // 2 * PI * 40

  function update() {
    const now = new Date().getTime();
    const gap = targetTime - now;

    if (gap <= 0) {
      clearInterval(countdownInterval);
      $('#countdown').html('<div class="text-xl font-semibold text-gold">The Wedding Celebrations Have Begun!</div>');
      return;
    }

    const sec = 1000;
    const min = sec * 60;
    const hour = min * 60;
    const day = hour * 24;

    const dVal = Math.floor(gap / day);
    const hVal = Math.floor((gap % day) / hour);
    const mVal = Math.floor((gap % hour) / min);
    const sVal = Math.floor((gap % min) / sec);

    // Update Numerical Content
    $('#days-val').text(dVal < 10 ? '0' + dVal : dVal);
    $('#hours-val').text(hVal < 10 ? '0' + hVal : hVal);
    $('#minutes-val').text(mVal < 10 ? '0' + mVal : mVal);
    $('#seconds-val').text(sVal < 10 ? '0' + sVal : sVal);

    // Update Circular Progress bars
    // Days uses base of 100 days maximum
    const daysOffset = circumference - (Math.min(dVal, 100) / 100) * circumference;
    const hoursOffset = circumference - (hVal / 24) * circumference;
    const minsOffset = circumference - (mVal / 60) * circumference;
    const secsOffset = circumference - (sVal / 60) * circumference;

    $('#days-circle').css('stroke-dashoffset', daysOffset);
    $('#hours-circle').css('stroke-dashoffset', hoursOffset);
    $('#minutes-circle').css('stroke-dashoffset', minsOffset);
    $('#seconds-circle').css('stroke-dashoffset', secsOffset);
  }

  update();
  const countdownInterval = setInterval(update, 1000);
}

